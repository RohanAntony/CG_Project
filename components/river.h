void river(int x,int y,float scaleX=1.0,float scaleY=1.0,int rotationX=0,int rotationY=0,int rotationZ=0){
	glTranslatef(x,y,0);
	glRotatef(rotationX,1,0,0);
	glRotatef(rotationY,0,1,0);
	glRotatef(rotationZ,0,0,1);
	glScalef(scaleX,scaleY,1);

	//river border
	glBegin(GL_LINE_STRIP);
		glColor3f(0,0,0);
		glVertex2d(-15,480);
		glVertex2d(-85,410);
		glVertex2d(-15,270);
		glVertex2d(-175,30);
		glVertex2d(-65,-190);
		glVertex2d(-215,-480);
	glEnd();
	glBegin(GL_LINE_STRIP);
		glColor3f(0,0,0);
		glVertex2d(45,480);
		glVertex2d(-5,400);
		glVertex2d(105,270);
		glVertex2d(35,40);
		glVertex2d(185,-190);
		glVertex2d(145,-460);
	glEnd();

	//water in river
	glBegin(GL_POLYGON);
		glColor3f(0.4,0.4,0.9);
		glVertex2d(-15,480);
		glVertex2d(45,480);
		glVertex2d(-5,400);
		glVertex2d(-85,410);
	glEnd();
	glBegin(GL_POLYGON);
		glColor3f(0.4,0.4,0.9);
		glVertex2d(-85,410);
		glVertex2d(-5,400);
		glVertex2d(105,270);
		glVertex2d(-15,270);
	glEnd();
	glBegin(GL_POLYGON);
		glColor3f(0.4,0.4,0.9);
		glVertex2d(-15,270);
		glVertex2d(105,270);
		glVertex2d(35,40);
		glVertex2d(-175,30);
	glEnd();
	glBegin(GL_POLYGON);
		glColor3f(0.4,0.4,0.9);
		glVertex2d(-175,30);
		glVertex2d(35,40);
		glVertex2d(185,-190);
		glVertex2d(-65,-190);
	glEnd();
	glBegin(GL_POLYGON);
		glColor3f(0.4,0.4,0.9);
		glVertex2d(-65,-190);
		glVertex2d(185,-190);
		glVertex2d(145,-460);
		glVertex2d(-215,-480);
	glEnd();

	glScalef(1/scaleX,1/scaleY,1);
	glRotatef(-rotationZ,0,0,1);
	glRotatef(-rotationY,0,1,0);
	glRotatef(-rotationX,1,0,0);
	glTranslatef(-x,-y,0);
}
