		glVertex2d(0,20);
		glVertex2d(300,0);
		glVertex2d(0,-20);
		glVertex2d(0,10);
	glEnd();
	glBegin(GL_LINE_STRIP);
		glColor3f(0,0,0);
		glVertex2d(0,20);
		glVertex2d(300,0);
		glVertex2d(0,-20);
